vector<int> subtree_size;
vector<int> parent_;
void dfs(int s, vector<vector<int>> & adj, int parent){
	subtree_size[s] = 1;
	parent_[s] = parent;
	for(auto v : adj[s]){
		if(v != parent){
			dfs(v, adj, s);
			subtree_size[s] += subtree_size[v];
		}
	}
	return;
}

void find_centroid(vector<vector<int>> &adj, vector<int> &centroids){
	int n = adj.size()-1;
	subtree_size.clear(); parent_.clear();
	subtree_size.resize(n+1); parent_.resize(n+1);
	dfs(1, adj, 0);
	//debug_arr(subtree_size);
	for(int i=1; i<=n; ++i){
		
			bool flag = true;		
			for(auto v : adj[i]){
				//p2(i, v);
				if( v == parent_[i]) {
					if(n-subtree_size[i] > n/2){
						flag = false;
						break;
					}
					
				}
				else if(subtree_size[v] > n/2){
					flag = false;
					break;
				}
			}

			if(flag){
				centroids.push_back(i);
			}

		}
	

	return;

}


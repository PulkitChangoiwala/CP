void floyd_warshall(vector<vector<int>> &dis, int n){
	for(int k=1;k<=n;++k){
		for(int i=1;i<=n;++i){
			for(int j=1;j<=n;++j){
				dis[i][j] = min(dis[i][j], dis[i][k]+dis[k][j]);
			}
		}

	}
}

void dijkstra(vector<vector<pair<int, int>>> &adj, vector<int> &dis, int u){

	int n = adj.size();
	dis.assign(n,inf);
	//parent.assign(n,-1); //For Path Calculation
	//dis[u] = 0;
	set<pair<int,int>> s;
	//s.insert({dis[u],u});


	for(auto edge : adj[u]){
		int to = edge.first;
		int weight = edge.second;
		dis[to] = min(dis[to],weight);
		s.insert({weight, to});
	}


	while(!s.empty()){

		int v = s.begin()->second;
		s.erase(s.begin());
		
		for(auto edge : adj[v]){
			int to = edge.first;
			int weight = edge.second;
	
			if(dis[v] + weight < dis[to]){
				s.erase({dis[to], to});
				dis[to] = dis[v] + weight;
				//parent[to] = v;
				s.insert({dis[to], to});

			}
		}



	}

	return;


}


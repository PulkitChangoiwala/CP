string st;

void update(int s){
	if(adj[s].size()==0){
		possible[s] = st[s] == '?' ? 2 : 1;
		
	}
	else
	{	
		int l = adj[s][1]; int r = adj[s][0];
		if(st[s]=='?') {
			possible[s] = possible[l]+possible[r];
		}
		else if(st[s]=='1') {
			possible[s] = possible[max(r,l)];			
		}
		else {
			possible[s] = possible[min(r,l)];			
		}

	}
	return;	
}
void dfs(int s, int p){
	parent[s] = p;

	for(auto v : adj[s]){
		if(v!=p){
			dfs(v,s);
		}
	}
	update(s);
}
void query(int s, char ch){
	if(st[s]==ch) return;
	st[s] = ch;
	while(1){
		update(s);
		if(parent[s]==s) break;
		s = parent[s];
	}


	
	return;
}

/*

- Suppose we for all x, we know all y can go 
	
Then for x+1
	- y = x, (via shift of first kind)
	- y = 1, (via shift of second kind, (x+1)/(x+1))
	- Let i be a divisor of x+1, (i!=1)
		say, (x+1)/i = q then x/i = q-1
	- So for each divisor i of x+1, we subtract dp[i-1] and add dp[i]


Now i is divisor of maxN/i numbers
	- When we calculate dp[i], we add it to all of its multiple, and
		dp[i-1] from all of its multiples

n/2 + n/3 .... n/n = O(nlogn)

*/
void solve(){ 
	int n,m;
	cin >> n >> m;
	mod = m;

	vector<int> dp(n+1);    //prefix sum dp
	dp[1] = 1;

	for(int i=2; i<=n; ++i){

		//with first shift we can go from i-1 to 1
		//ans[i] = ans[i-1] + ans[i-1] + ans[1] + extra[i], as proved above
		int ansi_1 = dp[i-1]-dp[i-2]+mod;
		dp[i] = (dp[i] + dp[i-1]+ 2*ansi_1 + dp[1])%mod;  

		if(i==2) dp[i] = 3;

		for(int j=2*i; j<=n; j+=i){
			//dp[i] contributes in all of its multiple by a factor of ->
			//dp[i] - dp[i-1] - dp[i-1] + dp[i-2]
			dp[j] = (dp[j]+dp[i]+dp[i-2]-2*dp[i-1]+mod)%mod;
		}

	}
	// debug(dp);

	int ans = ad(dp[n], -dp[n-1]);
	p1(ans);
return;} // solve ends 




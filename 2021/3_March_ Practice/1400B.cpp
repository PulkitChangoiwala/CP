void solve(){ 
	
	int p,f;
	cin>>p>>f;
	int cs,cw;
	cin>>cs>>cw;
	int s,w;
	cin>>s>>w;
	int ans = 0;
	int i_f = 0, j_f = 0, j =0;
	//iterate over number of swords taken by me
	for(int i=0;i<=cs;++i){ 
		int temp = i*s;
		if(p - temp>=0){
			j = ((p-temp)/w);//number of axes taken by me
			j = min(cw, j);
			if(s<w){
				i_f = min(cs-i, f/s);
				j_f = min(cw-j, max(0ll, (f - i_f*s)/w));
			}
			else{
				j_f = min(cw-j, f/w);
				i_f = min(cs-i, max(0ll, (f - j_f*w)/s));
			}
			ans = max(ans, i_f+j_f+i+j);
		}
		
	}

	p1(ans);


return;} // solve ends 




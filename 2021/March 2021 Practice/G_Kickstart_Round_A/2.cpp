int check(int i, int j,int i1, int j1, vector<int> &v){
	if(v[i] < 2 || v[j] < 2) return 0;
	int y = max(v[i], v[j]);
	int x = min(v[i], v[j]);
	return ((min(y/2,x) -1) + max(x/2, 1ll)-1);
	//if(v[i1]>1 || v[j1] > 1) return false;
}

int solve(){ 
	

	int r,c;
	cin >> r >> c;
	vector<vector<int>> mat(r, vector<int>(c));
	for(int i=0;i<r;++i){
		for(int j=0; j<c; ++j){
			cin>>mat[i][j];
		}
	}
	vector<vector<vector<int>>> cnt(r, vector<vector<int>>(c, vector<int>(4)));
	// 0 +x, 1 +y, 2 -x, 3 -y
	for(int i=0;i<r;++i){
		for(int j=0; j<c; ++j){
			cnt[i][j][2] = mat[i][j];
			if(j!=0) {
				cnt[i][j][2] = (mat[i][j] == 1)*cnt[i][j-1][2] + mat[i][j]; 
			}
		}
	}

	for(int j=0;j<c;++j){
		for(int i=r-1; i>=0; --i){
			cnt[i][j][3] = mat[i][j];
			if(i!=r-1) {
				cnt[i][j][3] = (mat[i][j] == 1)*cnt[i+1][j][3] + mat[i][j]; 
			}
		}
	}

	for(int i=r-1;i>=0;--i){
		for(int j=c-1; j>=0; --j){
			cnt[i][j][0] = mat[i][j];
			if(j!=c-1) {
				cnt[i][j][0] = (mat[i][j] == 1)*cnt[i][j+1][0] + mat[i][j]; 
			}
		}
	}


	for(int j=c-1;j>=0;--j){
		for(int i=0; i<r; ++i){
			cnt[i][j][1] = mat[i][j];
			if(i!=0) {
				cnt[i][j][1] = (mat[i][j] == 1)*cnt[i-1][j][1] + mat[i][j]; 
			}
		}
	}
/*	for(int i=0;i<r;++i){
		for(int j=0;j<c;++j){
			for(int k=0;k<4;++k){

				cout<<cnt[i][j][k]<<" ";

			}
			cout<<" || ";
		}
		cout<<endl;
	}*/

	int ans =0;
	int temp = 0;
	for(int i=0;i<r;++i){
		for(int j=0; j<c; ++j){
			//i,j as endpoint
			temp  = 0;
			vector<int> v(cnt[i][j]);
			// 0 +x, 1 +y, 2 -x, 3 -y
			//1st Quad
			temp += check(0,1,2, 3, v);
			//2nd quad
			//if(check(2,1,0,3,v)) temp++;
			temp += check(2,1,0,3,v);
			//3rd Quad
			//if(check(2,3,0,1,v)) temp++;
			temp += check(2,3,0,1,v);
			//4th Quad
			//if(check(0,3,1,2,v)) temp++;
			temp += check(0,3,2,1,v);
			//cout<<temp<<" ";
			ans += temp;
		}
		//cout<<endl;
		
	}


return ans;} // solve ends 




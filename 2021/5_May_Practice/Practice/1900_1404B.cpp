int mx,a1,disab;
vector<vector<int>> adj;

void dfs(int s, int p, int d, int b){
	if(s==b) {disab = d;}
	if(d>mx) {mx=d; a1 = s;}
	for(auto v : adj[s]){
		if(v!=p){
			dfs(v,s,d+1,b);
		}
	}
}


vector<vector<int>> adj;
vector<int> topo;
vector<bool> vis;
vector<int> component;
int n,m,N;

void dfs(int s){

	vis[s] = true;
	for(auto v : adj[s]){
		if(!vis[v]){
			dfs(v);

		}
	}
	topo.push_back(s);
}

void dfs1(int s, int c, vector<vector<int>> &adj_){
	vis[s] = true;
	component[s] = c;
	for(auto v : adj_[s]){
		if(!vis[v]){
			dfs1(v,c,adj_);
		}
	}
}


void strongly_connected(){
	component.clear();
	topo.clear();
	vis.clear();
	component.resize(n+1);
	vis.resize(n+1, false);

	for(int i=1;i<=n;++i){
		if(!vis[i]){
			dfs(i);
		}
	}
	reverse(topo.begin(), topo.end());

	int c = 1;
	vector<vector<int>> adjr(n+1);

	vis.assign(n+1, false);

	for(int i=1;i<=n;++i){
		for(auto v : adj[i]){
			adjr[v].push_back(i);
		}
	}

	for(auto v : topo){
		if(!vis[v]){
			dfs1(v,c,adjr);
			c++;
		}
	}

	return;

}

int neg(int x, int m){
	if(x<=m) {
		return (x+m);
	}
	else return x-m;
}


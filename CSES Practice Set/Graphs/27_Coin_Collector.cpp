void dfs(int s){ vis[s] = true; 
	for(auto v : adj[s]){ if(!vis[v])  dfs(v);}
	topo.push_back(s);
}

void dfs1(int s, int c, vector<vector<int>> &adj_){ component[s] = c;
	for(auto v : adj_[s]){ if(component[v] == -1){ dfs1(v,c,adj_);}}
}


void strongly_connected(){
	
	topo.clear();
	component.assign(n+1, -1);
	vis.assign(n+1, false);

	for(int i=1; i<=n; ++i){
		if(!vis[i]) dfs(i);
	}

	reverse(topo.begin(), topo.end());
	vector<vector<int>> adjr(n+1);

	for(int i=1; i<=n; ++i){
		for(auto v : adj[i]){
			adjr[v].push_back(i);
		}
	}
	c = 0;
	for(auto v : topo){
		if(component[v] == -1) {
			c++;
			dfs1(v,c,adjr);
		}
	}
	return;
}

void dfs3(int s){
	vis[s] = true;
	for(auto v : adj1[s]){
		if(!vis[v]) 
			dfs3(v);
	}
	topo.push_back(s);
}


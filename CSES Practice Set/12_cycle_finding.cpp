int n,m;
vector<vector<int>> edges;
vector<int> parent;
vector<int> dis;
vector<int> cycle;
set<int> st;
const int inf = 1e16;
int start;
int flag;

bool bellaman_ford(int s){

	int u,v,w;
	bool is_changed = 0;
	dis.clear();
	dis.resize(n+1, inf);
	dis[s] = 0;

	for(int i=0;i<n;++i){
		is_changed = 0;
		for(int i=0;i<m;++i){
			u = edges[i][0];
			v = edges[i][1];
			w = edges[i][2];
			if(dis[v] > dis[u]+w){
				dis[v] = dis[u]+w;
				parent[v] = u;
				start = v; 
				is_changed  = 1;
			}
			
		}

		if(!is_changed) break;
	}

	if(is_changed){
		//negative cycle
		//start is the last vertex which is changed, need not to be in cycle, let's visit its ancestors
		for(int i=0;i<n;++i) start = parent[start];
		for(int v_itr = start; ;v_itr = parent[v_itr]){
		 	cycle.push_back(v_itr);
		 	if(v_itr == start && cycle.size()>1) break;
		 }	
		 reverse(cycle.begin(),cycle.end());
	}
	
	return is_changed;
}



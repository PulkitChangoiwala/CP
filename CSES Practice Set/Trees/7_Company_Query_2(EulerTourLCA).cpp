class ST{
int n;
vector<int> t;

public:
    ST(int n, vector<int> &arr) {this->n = n; t.resize(2*n);
        for(int i=n; i<2*n; ++i) t[i] = i-n;
    }
    
    void build(vector<int> &arr){
        for(int i=n-1; i>=1; i--) t[i] = arr[t[i<<1]] < arr[t[i<<1|1]]? t[i<<1] : t[i<<1|1];
      //  debug_arr(t);

    }
    int query(int l, int r, vector<int> &arr){
        int res = l;
       for(l+=n,r+=n; l<r;l>>=1,r>>=1) {
           if(l&1) {res = arr[res]<arr[t[l]] ? res : t[l]; l++;}
           if(r&1) {--r; res = arr[res]<arr[t[r]]?res:t[r];}
       }
       return res;
    }
};
void dfs(int s, int p, int d){
    euler_tour.push_back(s);
    height.push_back(d);
    for(auto v : adj[s]) {
        if(v!=p) { 
            dfs(v,s,d+1);
            euler_tour.push_back(s);
            height.push_back(d);
        }
    }
}

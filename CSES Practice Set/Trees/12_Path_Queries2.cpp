void build(){
	for(int i = n-1; i>=1; --i) t[i] = max(t[i<<1],t[i<<1 | 1]);
}

int query(int l, int r){ //[l,r)
	int res = 0;
	for(l+=n, r+=n; l<r; l >>= 1, r >>=1){
		if(l&1) res = max(res,t[l++]);
		if(r&1) res = max(res,t[--r]); 
	}
	return res;

}

void modify(int ind, int value){
	for(t[ind+=n]=value; ind >1; ind >>= 1) t[ind >> 1] = max(t[ind], t[ind^1]);
}

bool is_ancestor(int u, int v)
{
    return tin[u] <= tin[v] && tout[v] <= tout[u];
}

int lca(int u, int v)
{
    if (is_ancestor(u, v))
        return u;
    if (is_ancestor(v, u))
        return v;
    for (int i = l; i >= 0; --i) {
        if (ancestors[u][i]!=0 && !is_ancestor(ancestors[u][i], v))
            u = ancestors[u][i];
    }
    return ancestors[u][0];
}

void dfs_sz(int s = 1, int p = 0){
	ancestors[s][0] = p;
	size[s]++;
	for(auto &v : adj[s]){
		if(v==p) {swap(v,adj[s][adj[s].size()-1]);}
		if(v!=p) {
			dfs_sz(v,s);
			size[s] += size[v];
			if(size[v] > size[adj[s][0]]) swap(v, adj[s][0]);
		}
	}
}

void dfs_hld(int s = 1, int p = 0){
	tin[s] = ctr++;
	dfs.push_back(s);
	parent[s] = p;
	for(auto v:adj[s]){
		if(v!=p){
			path_start[v] = (v == adj[s][0]) ? path_start[s] : v;
			dfs_hld(v,s);
		}
	}
	tout[s] = ctr;
}

void preprocess(int root) {
    tin.resize(n+1);
    tout.resize(n+1);
    size.resize(n+1);
    parent.resize(n+1);
    path_start.resize(n+1);
    path_start[root] = root;
    ctr = 0;
    l = ceil(log2(n));
    ancestors.assign(n+1, vector<int>(l + 1));
    dfs_sz();
    dfs_hld();
    for(int i=1; i<=l; ++i){
        for(int j=1; j<=n; ++j) {
            ancestors[j][i] = ancestors[ancestors[j][i-1]][i-1];
        }
    }
}

int find(int b, int a){
	int pt;
	int ans = -1;
	while(1){
		pt = path_start[a];
		if(is_ancestor(pt,b)) {
			ans = max(ans, query(tin[b], tin[a]+1));
			break;
		}
		else {
			ans = max(ans, query(tin[pt], tin[a]+1));
			a = parent[pt];

		}
		if(a==0) break;
		
	}
	return ans;

}


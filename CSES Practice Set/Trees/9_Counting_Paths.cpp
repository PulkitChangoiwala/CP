//Using Difference Array On Trees

int n,m,ctr;
vector<vector<int>> adj;
vector<vector<int>> ancestors;
vector<int> ans,tin, tout;

void dfs(int s=1, int p=0){
	ancestors[s][0] = p;
	tin[s] = ++ctr;
	for(auto v : adj[s]){
		if(v!=p) dfs(v,s);
	}
	tout[s] = ++ctr;
}

int dfs1(int s=1, int p=0){
	int res = 0;
	for(auto v : adj[s]){
		if(v!=p){
			res += dfs1(v,s);
		}
	}
	ans[s] += res;
	return ans[s];
}

bool isanc(int a, int b){
	return tin[a] <= tin[b] && tout[b] <= tout[a];
}

int lca_(int a, int b){
	if(isanc(a,b)) return a;
	if(isanc(b,a)) return b;
	int l = log2(n)+1;
	for(int i=l; i>=0; --i){
		if(ancestors[a][i]==0 || isanc(ancestors[a][i],b)) continue;
		a = ancestors[a][i];
	}
	return ancestors[a][0];

}


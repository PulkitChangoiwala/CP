int n,m;
vector<vector<int>> t;

void build(){
	for(int i=n; i<2*n; ++i)
		for(int j=m-1; j>=1; --j)
			t[i][j] = t[i][j<<1] + t[i][j<<1|1];
	
	for(int j=1; j<2*m; ++j) 
		for(int i=n-1; i>=1; --i)
			t[i][j] = t[i<<1][j]+t[i<<1|1][j];

}

int query(int l1, int r1, int l2, int r2){
	int L1 = l1, R1 =r1;
	int res = 0;
	for(l2+=m, r2+=m; l2<r2; l2>>=1,r2>>=1){

		if(l2&1){
			L1 = l1; R1 =r1;
			for(L1+=n,R1+=n; L1<R1; L1>>=1,R1>>=1){
				if(L1&1) {res += t[L1++][l2];}
				if(R1&1) {res += t[--R1][l2];}
			}
			l2++;
		}
		if(r2&1){
			--r2;
			L1 = l1; R1 =r1;
			for(L1+=n,R1+=n; L1<R1; L1>>=1,R1>>=1){
				if(L1&1) {res += t[L1++][r2];}
				if(R1&1) {res += t[--R1][r2];}
			}
		}

	}
	return res;
}

void update(int i, int j){
	i+=n; j+=m;
	int  J = j, I=i;

	t[i][j] = 1-t[i][j];  //new value


	for(j=J;j>1;j>>=1) t[i][j>>1] = t[i][j] + t[i][j^1];
	
	for(i=I;i>1; i>>=1){
		for(j=J;j>=1; j>>=1) 
			t[i>>1][j] = t[i][j] + t[i^1][j];	
	}
	 	
}


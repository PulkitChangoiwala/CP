int n,q;
vector<pr> t;

void build(){
	for(int i = n-1; i>=1; i--)  {
		t[i].f = t[i<<1].f + t[i<<1|1].f;
		t[i].s = max(t[i<<1].s, t[i<<1].f+t[i<<1|1].s);
 	}
}

void update(int i, int val){
	t[i+n].f = val;
	t[i+n].s = max(val,0ll); 
	for(i+=n;i>1; i>>=1){
		t[i>>1].f = t[i].f + t[i^1].f;
		t[i>>1].s = max(t[min(i,i^1)].s, t[min(i,i^1)].f+t[max(i,i^1)].s);

	}
}


int query(int l, int r, int &sum ){
	int L=l, R = r;
	int sum_ = 0;
	sum = 0; 
	int res = 0;
	
	//base cases

	if(l>=r) return 0;
	if(l==r-1) {
		sum = t[l].f;
		return t[l].s;
	}
	

	if(l&1) { sum+=t[l].f; res = max(res,t[l].s); l++;  } //l is right child	
	if(r&1) { --r;  sum+=t[r].f; } //r-1 is left child
	int res1 = query(l>>1, r>>1, sum_);
	sum += sum_;
	if(L&1)	{
		res = max(res, t[L].f+res1);
		res = max(res, sum); 
		if(R&1) res = max(res, t[L].f+sum_+t[R-1].s); 
	}
	else {
		res = max(res, res1);
		res = max(res, sum); 
		if(R&1) res = max(res, sum_+t[R-1].s); 

	}
	

	return res;

}


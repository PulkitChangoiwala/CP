/*
Algo 1:
- Coordinate Compression.
	- No update: 
		- Suppose we do not have any updates and we sort the salary array 
		- Store frequency of each salary & Remove Duplicates. 
		- Now index of an salary represents compressed coordinate of it.
		- For salary in range [a,b], we search index of a & b in the sorted array. Say indices are i & j
		- We sum frequency array from frequency[i] to frequency[j]. For this we use segment tree.
	- Now assume we have updates:
		- And suppose an new salay is introduced(which didn't exist earlier). We do not have any index for such salary(or say compressed coordinate).
		- Thus it leades to failure of our solution
		- *** SOLUTION *** 
		- In the first step itself find the compressed coordinate of all unique salary values(atmost n+q unique values).
		- For this read all queries before building segment tree.

Algo 2:
- Divide 10^9 into 10^7 intervals of length 100 each. And build segment trees on these intervals.

*/

vector<int> f;
vector<pr> queries;
vector<int> salary;
int n,q;

void build(){
	for(int i = n-1; i>=1; --i) f[i] = f[i<<1] + f[i<<1|1];
}

void update(int i, int val){
	for(f[i+=n] += val; i>1; i>>=1) f[i>>1] = f[i] + f[i^1];
}

int query(int l, int r){
	int res = 0;
	for(l+=n, r+=n; l<r; l>>=1, r>>=1){
		if(l&1) res += f[l++];
		if(r&1) res += f[--r];
	}
	return res;
}

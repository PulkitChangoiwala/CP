class ST{ //Min seg tree
	int n;
	vector<int> t;
public:
	ST(int a){ n = a; t.resize(4*n);}
	void build(int st, int end, int v, vector<int> & arr){
		if(st == end) {t[v] = arr[st]; return;}
		int mid = (st+end)/2;
		build(st, mid, 2*v, arr);
		build(mid+1, end, 2*v+1, arr);
		t[v] = min(t[2*v],t[2*v+1]);
		return;
	}

	void update(int tl, int tr, int v, int pos, int val){
		if(tl==tr) {t[v] = val; return;}
		int tm = (tl+tr)/2;
		if(pos<=tm) update(tl, tm, 2*v, pos, val);
		else update(tm+1, tr, 2*v+1, pos, val);
		t[v] = min(t[2*v],t[2*v+1]);
	}

	int query(int tl, int tr, int v, int l , int r){
		if(l>r) return 1e18;
		if(tl == l && tr == r) return t[v];
		int tm = (tl+tr)/2;
		return min(query(tl, tm, 2*v, l, min(r, tm))
		,query(tm+1,tr, 2*v+1, max(l,tm+1), r));
	}

};

class STi{
	int n;
	vector<int> t;
public:
	STi(int n) {this->n = n; t.resize(2*n);}
	void build(vector<int>&arr) {
		for(int i=n;i<2*n;++i) t[i] = arr[i-n];
		for(int i = n-1; i>=1; i--) t[i] = min(t[i<<1], t[i<<1|1]);
	}
	void update(int i, int val) {
		for(t[i+=n] = val; i>1; i>>=1) t[i>>1] = min(t[i], t[i^1]);
	}
	int query(int l, int r){ //[l,r)
		int res = 1e18;
		for(l+=n,r+=n; l<r; l>>=1, r>>=1){
			if(l&1) res = min(res, t[l++]);
			if(r&1) res = min(res, t[--r]);
		}
		return res;
	}
};


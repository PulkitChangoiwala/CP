const int  mod = 1e9 +7;
const int mod1= 998244353;
int fact[200001], invFact[200001];

int C(int n, int r){

	if(n<r) return 0;
	return fact[n] * invFact[r] % mod * invFact[n-r] % mod;

}


int fast_pow(int x, int y){

	int res = 1;
	while(y){
		if(y&1) {res = res*x%mod;y--;}
		else {x = x*x%mod; y/=2;}
	}
	return res;
}


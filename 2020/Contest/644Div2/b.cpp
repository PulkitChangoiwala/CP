void completerow(vector<pr> &ans, int dir){
	pr last = ans[ans.size()-1];
	int x,y;
	if(last.s==m){
		if(last.f==n){
			x=sx-1;
			y=m;
		}
		else {
			x=last.f+dir;
			y=m;
		}

		while(y>=1){
			ans.pb({x,y});
			y--;
		}

	}
	else if(last.s==1){

		if(last.f==n){
			x=sx-1;
			y=1;
		}
		else {
			x=last.f+dir;
			y=1;
		}


		while(y<=m){
			ans.pb({x,y});
			y++;
		}

	}

return;

}


